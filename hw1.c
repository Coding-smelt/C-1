#include <stdio.h>


int main(void) {

	//개념 확인 과제
	int num1, num2;
	printf("Input two integers: ");
	scanf("%d", &num1);
	scanf("%d", &num2);
	printf("%d & %d = %d\n", num1, num2, num1 & num2);
	printf("%d | %d = %d\n", num1, num2, num1 | num2);
	printf("%d ^ %d = %d\n", num1, num2, num1 ^ num2);






}